    private void metodoConfirmarEliminacion() {
        // Crear una instancia de Usuario
        Usuario objeto = new Usuario();

        // Obtener el valor del campo de texto para la identificación
        String identificacionStr = textIdentificacion.getText();
        int identificacion;

        // Validar y convertir la identificación a entero
        try {
            identificacion = Integer.parseInt(identificacionStr);

            // Verificar que la identificación sea mayor que cero
            if (identificacion <= 0) {
                JOptionPane.showMessageDialog(this, "La identificación debe ser mayor que cero.");
                return; // Salir del método si la identificación no es válida
            }
        } catch (NumberFormatException e) {
            // Mostrar mensaje de error si la identificación no es un número entero válido
            JOptionPane.showMessageDialog(this, "La identificación debe ser un número entero válido.");
            return; // Salir del método si la conversión falla
        }

        // Asignar la identificación al objeto Usuario usando el método setter adecuado
        objeto.setIdentificacion_Pk(identificacion);

        // Instanciar el controlador y ejecutar el método para eliminar el usuario
        metodoCliente metodo = new metodoCliente();
    
        // Llamar al método eliminarUsuario pasando el objeto Usuario
        if (metodo.eliminarCliente(identificacion, this)) {
            // Mostrar mensaje de éxito si el usuario se eliminó correctamente
            JOptionPane.showMessageDialog(this, "Cliente eliminado exitosamente.");

            // Cambio a la vista de administración de usuarios
            ClientesAdmin vista = new ClientesAdmin();
            vista.setVisible(true);
            this.setVisible(false);
        } else {
            // Mostrar mensaje de error si no se pudo eliminar el usuario
            JOptionPane.showMessageDialog(this, "Error al eliminar el usuario.");
        }   
    }

